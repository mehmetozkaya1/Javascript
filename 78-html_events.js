/*
    HTML DOM (eventhandler) kullanarak belirli olaylar meydana geldiğinde belirli komutların çalışmasını sağlayabiliriz.

    Örenğin, fare ile bir butona tıklanıldığında bir alert kutusu açabiliriz.

    Ya da sayfa açıldığında (onload olayı) belirli kodların çalışmasını sağlayabiliriz.

    Bazı HTML eventleri:

    * onload = Sayfanın yüklenmesi bittiğinde
    * onunload = Sayfanınn kapanması
    * onresize = sayfa boyutu değiştiğinde
    * onerror = Sayfada bir JS hatası olduğunda
    * onfocus = Bir form elemanı aktif olduğunda
    * onblur = Bir form elemanı pasif olduğunda
    * onchange = Bir form elemanının değeri değiştiğinde
    * onsubmit = Form onaylandığında
    * onreset = Form resetlendiğinde
    * oninvalid = Forma girilen değer geçerli değilse
    * oninput = Forma veri girilirse
    * onclick = Bir elemana tıklandığında
    * ondblclick = Bir elemana çift tıklandığında
    * ondrag = Bir eleman sürüklendiğinde
    * onmouseover = Bir elemanın üzerine fare geldiğinde
    * onmouseout = Fare eleman dışına çıktığında
*/