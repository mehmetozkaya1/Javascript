/*
    Window nesnesi hiyerarşinin en üstünde yer alır ve tarayıcımızı temsil eder.

    Document, Screen, Location, History ve Navigator nesneleri Window nesnesinin altında yer alır.

    Bazı Window özellik ve metotları:

    * window.name = Pencereye bir isim verir.
    * window.innerHeight = Pencere yüksekliğini döndürür.
    * window.innerWidth = Pencere genişliğini döndürür.
    * window.alert() = Mesaj kutusunu çalıştırır ve bir ileti gösterir.
    * window.prompt() = Kullanıcıdan bir veri alır.
    * window.print() = Sayfaya yazdırır.
    * window.open(URL) = Belirtilen URL adresini açar.
    * window.close() = Aktif pencereyi kapatır.
    * window.confirm() = Onay kutusunu çalıştırır. Tamama tıklanırsa true, İptale basılırsa false döner.
    * window.focus() = Farenin açık veya open() metodu ile açılan pencereye odaklanmasını sağlar.
    * window.setInterval() = Bir fonksiyonun belirli bir süre aralıkla tekrar çalışmasını sağlar.
    * window.clearInterval() = setInterval() metodunu iptal eder.
    * window.setTimeout() = Bir fonksiyon ya da metodun belirli bir süre sonunda sadece bir defa çalışmasını sağlar.
    * window.clearTimeout() = setTimeout() metodunu iptal eder.
    * window.blue() = Farenin odaklanmasını iptal eder.
*/